def process_s3_financial_statements(s3_key_prefix, company_id_map, access_key, secret_key):
    """
    處理 S3 中特定前綴下的財報檔案。
    Args:
        s3_key_prefix (str): S3 物件的前綴，用於篩選檔案。
        company_id_map (dict): 股票代碼到公司ID的映射字典。
        access_key (str): AWS Access Key ID.
        secret_key (str): AWS Secret Access Key.
    """
    if not company_id_map:
        print("公司ID映射為空，無法處理財報資料。請提供有效的 company_id_map。")
        return

    # 確保本地暫存目錄存在
    os.makedirs(BASE_TEMP_DIR, exist_ok=True)
    print(f"本地暫存目錄已設置為: {os.path.abspath(BASE_TEMP_DIR)}")

    try:
        # 使用 boto3.resource 來列出物件
        s3_resource = boto3.resource(
            's3',
            region_name=S3_REGION_NAME,
            aws_access_key_id=access_key,
            aws_secret_access_key=secret_key
        )
        bucket = s3_resource.Bucket(S3_BUCKET_NAME)
        
        # 列出指定前綴的所有物件
        objects = bucket.objects.filter(Prefix=s3_key_prefix)

        found_files = False
        for obj in objects:
            found_files = True
            s3_key = obj.key
            
            # 確保只處理 json 檔案且避免資料夾本身
            if not s3_key.endswith('.json'):
                continue

            print(f"\n處理檔案: {s3_key}")
            filename = os.path.basename(s3_key)
            stock_code, year_roc, quarter, report_kind, data_type_from_filename = parse_filename(filename)

            if not all([stock_code, year_roc, quarter, report_kind, data_type_from_filename]):
                print(f"錯誤: 無法解析檔案名稱 {filename}。跳過。")
                continue

            # 暫存到本地的檔案路徑
            local_json_path = os.path.join(BASE_TEMP_DIR, filename)

            # 將 access_key 和 secret_key 傳遞給 download_json_from_s3
            if download_json_from_s3(S3_BUCKET_NAME, s3_key, local_json_path, access_key, secret_key):
                try:
                    with open(local_json_path, 'r', encoding='utf-8') as f:
                        json_data = json.load(f)

                    # 轉換資料 (傳入 company_id_map)
                    transformed_record = transform_income_statement_data(json_data,
                                                                         (stock_code, year_roc, quarter, report_kind, data_type_from_filename),
                                                                         company_id_map)

                    if transformed_record:
                        # 插入資料庫
                        insert_income_statement_to_db(DB_CONFIG, transformed_record)
                    else:
                        print(f"檔案 {s3_key} 的資料轉換失敗。")

                except json.JSONDecodeError as e:
                    print(f"檔案 {local_json_path} 不是有效的 JSON 格式: {e}")
                except Exception as e:
                    print(f"處理檔案 {local_json_path} 時發生未預期的錯誤: {e}")
                finally:
                    # 處理完畢後刪除本地臨時檔案
                    if os.path.exists(local_json_path):
                        os.remove(local_json_path)
                        # print(f"已刪除本地臨時檔案: {local_json_path}")
            else:
                print(f"跳過檔案 {s3_key}，因為下載失敗。")
        
        if not found_files:
            print(f"S3 路徑 '{s3_key_prefix}' 下沒有找到任何可處理的檔案。")

    except Exception as e:
        print(f"列出或處理 S3 物件時發生錯誤: {e}")

